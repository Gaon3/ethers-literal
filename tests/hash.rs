use ethers::core::types::*;
use ethers_literal::hash;

#[test]
fn test() {
    hash! {
        assert_eq!(0x88e6A0c2dDD26FEEb64F039a2c41296FcB3f5640_H160, "0x88e6A0c2dDD26FEEb64F039a2c41296FcB3f5640".parse::<H160>().unwrap());
        assert_eq!(0x88e6A0c2dDD26FEEb64F039a2c41296FcB3f5640000000000000000000000000_H256, "0x88e6A0c2dDD26FEEb64F039a2c41296FcB3f5640000000000000000000000000".parse::<H256>().unwrap());
        assert_eq!(0x00000000000000000000000088e6A0c2dDD26FEEb64F039a2c41296FcB3f5640_H256, "0x00000000000000000000000088e6A0c2dDD26FEEb64F039a2c41296FcB3f5640".parse::<H256>().unwrap());
    }
}
